// Функция для получения рецептов по категории продукта
export function getRecipesByProductCategory(category: string): Recipe[] {
  return RECIPE_DATABASE.filter(recipe => recipe.mainIngredient === category);
}

// Функция для получения случайных рецептов
export function getRandomRecipes(count: number): Recipe[] {
  const shuffled = [...RECIPE_DATABASE].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}
